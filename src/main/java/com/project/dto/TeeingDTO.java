package com.project.dto;

public class TeeingDTO {
	
	private int teeingNum;
	private int roomNum;
	private String userId;
	private String name;
	private String email;
	private String ustoredFileName;
	private String title;
	private String content;
	private String created;
	
	
	public int getTeeingNum() {
		return teeingNum;
	}
	public void setTeeingNum(int teeingNum) {
		this.teeingNum = teeingNum;
	}
	public int getRoomNum() {
		return roomNum;
	}
	public void setRoomNum(int roomNum) {
		this.roomNum = roomNum;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getUstoredFileName() {
		return ustoredFileName;
	}
	public void setUstoredFileName(String ustoredFileName) {
		this.ustoredFileName = ustoredFileName;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getCreated() {
		return created;
	}
	public void setCreated(String created) {
		this.created = created;
	}
	
}
